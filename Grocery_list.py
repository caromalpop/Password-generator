#Creating a grocery list:
grocery_list = ["apples", "bananas", "carrots", "dates"]
print("Initial grocery list:", grocery_list)
#Adding an item to the list:
grocery_list.append("eggs")
print("After adding eggs:", grocery_list)
#Removing an item from the list:
grocery_list.remove("carrots")
print("After removing carrots:", grocery_list)
#Sorting the list:
grocery_list.sort()
print("Sorted grocery list:", grocery_list)
